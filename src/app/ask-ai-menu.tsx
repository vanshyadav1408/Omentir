export function AskAiIcon({
  bot,
  size = 18,
  gradientId = "askai-gemini",
}: {
  bot: AskAiBot;
  size?: number;
  /** Must be unique per rendered menu — a hidden duplicate id breaks the gradient fill. */
  gradientId?: string;
}) {
  const px = Math.round(size * (bot.iconScale ?? 1));
  return (
    <svg
      viewBox="0 0 24 24"
      width={px}
      height={px}
      aria-hidden="true"
      className={`shrink-0 ${bot.iconClassName ?? ""}`}
    >
      {bot.gradient ? (
        <defs>
          <linearGradient id={gradientId} x1="0" y1="24" x2="24" y2="0" gradientUnits="userSpaceOnUse">
            <stop offset="0" stopColor="#4285F4" />
            <stop offset="0.55" stopColor="#9B72CB" />
            <stop offset="1" stopColor="#D96570" />
          </linearGradient>
        </defs>
      ) : null}
      <path
        d={bot.iconPath}
        fill={bot.gradient ? `url(#${gradientId})` : bot.iconColor ?? "currentColor"}
      />
    </svg>
  );
}

